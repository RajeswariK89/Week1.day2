package week1.day2;

public class Calculator {
	
	public int add(int n1,int n2,int n3)
	{
		int addres = n1+n2+n3;
		return addres;
	}

	public int sub(int n1,int n2)
	{
		int subres = n1-n2;
		return subres;
	}
	
	public double mul(double n1,double n2)
	{
		double mulres = n1*n2;
		return mulres;
		
	}
	public float divide(float n1,float n2)
	{
		float divres = n1/n2;
		return divres;
	}
}
